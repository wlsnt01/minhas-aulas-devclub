/*
Operadores de Atribuição

= -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto 

*/

const valor = 20

valor += 10 // valor = valor + 10

valor -= 10 // valor = valor - 10

valor /= 10 // valor = valor / 10

valor *= 10 // valor = valor * 10

valor %= 10 // valor = valor % 10

// é uma forma de economizar codigo 